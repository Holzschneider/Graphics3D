package io.github.dualuse.awt.math;

public class AxisAngle4d {
    public double x;
    public double y;
    public double z;
    public double angle;

    public AxisAngle4d() {}

    public AxisAngle4d(double x, double y, double z, double angle) {
        this.x = x; this.y = y; this.z = z; this.angle = angle;
    }
}